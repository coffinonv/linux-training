# Install a package on Debian-based distribution
sudo apt install <package>

# Install a package on Red-Hat-based distribution
sudo yum install <package>

# Install a package on Arch-based distribution
sudo pacman -S <package>

# Update the package index file on Ubuntu
sudo apt update

# Update the actual packages installed on Ubuntu
sudo apt upgrade

# Install packages using snap (downloads also all dependencies)
sudo snap install <package>

# List installed packages on Ubuntu
sudo apt list --installed
